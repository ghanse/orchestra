_DB_SOURCE_TYPES = {
    "AzureSqlSource",
    "SqlServerSource",
    "OracleSource",
    "PostgreSqlSource",
    "MySqlSource",
    "SqlSource",
    "CosmosDbSqlApiSource",
    "SqlDWSource",
}


def generate_lookup_notebook(activity: LookupActivity) -> str:
    """Generate a Python notebook that executes a lookup query.

    Args:
        activity: The LookupActivity IR node.

    Returns:
        Complete notebook source code as a string.
    """
    header = _notebook_header(f"Lookup: {activity.name}")
    source_type = activity.source_type or ""
    query = activity.source_query or ""

    if source_type in _DB_SOURCE_TYPES:
        scope = f"orchestra-{activity.task_key}"
        body = textwrap.dedent(f"""\
            import json

            # Parameters
            first_row_only = dbutils.widgets.get("first_row_only") == "true"

            # Credentials
            jdbc_url = dbutils.secrets.get(scope="{scope}", key="jdbc-url")
            jdbc_password = dbutils.secrets.get(scope="{scope}", key="jdbc-password")
            jdbc_user = dbutils.secrets.get(scope="{scope}", key="jdbc-user")

            # Execute lookup query
            query = \"\"\"{query}\"\"\"

            df = (
                spark.read.format("jdbc")
                .option("url", jdbc_url)
                .option("user", jdbc_user)
                .option("password", jdbc_password)
                .option("query", query)
                .load()
            )

            if first_row_only:
                result = df.first()
                output = json.dumps(result.asDict() if result else {{}})
            else:
                rows = [row.asDict() for row in df.collect()]
                output = json.dumps(rows)

            dbutils.notebook.exit(output)
        """)
    else:
        body = textwrap.dedent(f"""\
            import json

            # Parameters
            first_row_only = dbutils.widgets.get("first_row_only") == "true"

            # Execute lookup query via Spark SQL
            query = \"\"\"{query}\"\"\"
            df = spark.sql(query)

            if first_row_only:
                result = df.first()
                output = json.dumps(result.asDict() if result else {{}})
            else:
                rows = [row.asDict() for row in df.collect()]
                output = json.dumps(rows)

            dbutils.notebook.exit(output)
        """)

    return header + _command_separator() + body


